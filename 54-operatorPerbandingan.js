//operator perbandingan
//biasa digunkan dalam kondisi if atau perulangan

function operatorPerbandingan() {
    var a = 10;
    var b = "10";


    //jika benar akan true jika salah akan false
    console.log(a < b); //kurang dari 
    console.log(a > b); //lebih besar dari
    console.log(a <= b); //kurang dari sama dengan
    console.log(a >= b); //lebih besar sama dengan
    console.log(a == b); //sama dengan
    console.log(a === b); //sama dengan type data juga harus sama
    console.log(a != b); //tidak sama dengan
    console.log(a !== b); //tidak sama dengan type data harus sama
}

operatorPerbandingan();