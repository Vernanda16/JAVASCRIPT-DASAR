//Penulisan function pada javascript ada tiga cara

//cara pertama (cara lama)
function contoh1() {
    console.log("ini contoh penulisan function javascript cara pertama");
}

//cara kedua (cara lama)
const contoh2 = function () {
    console.log("ini cara penulisan function javascript cara kedua");
}

//cara ketiga (cara baru)
const contoh3 = () => {
    console.log("ini cara penulisan function javascript cara ketiga atau cara baru");
}


contoh1();
contoh2();
contoh3();